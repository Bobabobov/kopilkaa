/** Для поиска: без регистра, дефисов, пробелов и прочих разделителей. */
export function normalizeBankSearchKey(value: string): string {
  return value
    .toLowerCase()
    .replace(/ё/g, 'е')
    .replace(/[^a-zа-я0-9]/gi, '');
}

